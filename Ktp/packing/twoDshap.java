package packing;

public class twoDshap {
          public double width;
          public double height;

          twoDshap (double w, double h) {
                    
                    width = w;
                    height = h;
          }

          public void showDim() {
                    System.out.println("width" + " : " + width);
                    System.out.println("height" + " : " + height);
          }
}
